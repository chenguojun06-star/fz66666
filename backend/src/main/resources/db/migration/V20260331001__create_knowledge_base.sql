  `id`           VARCHAR(64)   NOT NULL,
  `tenant_id`    BIGINT        DEFAULT NULL COMMENT '租户ID，NULL=公共知识（所有租户可见）',
  `category`     VARCHAR(32)   NOT NULL COMMENT '类别: terminology/sop/faq/system_guide/rule',
