'**工资结算规则**：

**结算触发时机**：
- 每月月末由财务主管在「财务结算 → 工资结算」模块发起对账
- 也可按订单完工后立即发起阶段性结算

**工资计算依据**：
工资 = Σ（工人扫码成功条数 × 对应工序件数 × 工序工价）

**完整计算示例**：
- 工人张三完成500件「车缝」工序，工价1.2元/件 → 贡献 600元
- 同月完成200件「锁边」工序，工价0.8元/件 → 贡献 160元
- 本月工资 = 600 + 160 = 760元

**特殊扣款规则**：
- 质检不合格返工的件数不计入工资
- 报废件数同样不计入工资
- 管理员可在结算单上增加「其他扣款」（如迟到/损坏工具等）

**工资已结算后能否撤回**：
❌ **不能撤回**。一旦扫码记录被纳入「已结算」状态（payrollSettled = true），该扫码记录无法撤销。
若发现数据错误，需联系管理员在下期结算中做补偿调整。

**查询工资明细**：
