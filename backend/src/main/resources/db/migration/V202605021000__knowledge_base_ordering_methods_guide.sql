'本系统支持 **6 种下单方式**，覆盖手动下单、AI建单、裁剪下单、外部对接等场景。

---

**方式一：PC端下单管理（最常用）**

入口：款式资料 → 下单管理 → 新建订单

操作步骤：
1. 在款式资料页找到目标款式，点击"下单"
2. 弹出下单弹窗，左侧展示款式封面和附件
3. 选择工厂：内部生产组（一车间/二组）或外发工厂
4. 填写下单时间、订单交期，可选标记为急单
5. 选择单型：首单（FIRST）或翻单（REORDER），不填则系统自动判断（查同款号历史订单）
6. 选择下单类型：FOB（包料包工）/ ODM（设计+生产）/ OEM（代工贴牌）/ CMT（纯加工）
7. 编辑颜色×尺码×数量明细表
8. 选择单价模式：工序单价（PROCESS）/ 尺码单价（SIZE）/ 外发整件单价（COST）/ 报价单价（QUOTE）/ 手动单价（MANUAL）
9. 可选填写：客户、品类、纸样师、跟单员、备注
10. 提交后系统自动：生成物料采购需求、异步生成CRM应收款、触发AI学习

问小云：「帮我给鑫达制衣厂下一单FZ2024001，红色S码100件XL码80件，交期5月30日」

---

**方式二：AI智能建单（最快捷）**

入口：小云对话框 → 自然语言描述

操作步骤：
1. 直接对小云说出需求，如"帮我建个生产订单"
2. 小云会引导你补充：款号/款名、工厂/生产组、颜色尺码数量、计划开始时间、交期
3. 必填项缺一不可，缺信息小云会追问
4. 确认后自动创建，触发物料采购+应收款

适用场景：跟单员快速建单，无需手动逐项填写表单

问小云：「帮我下单，款号FZ2024001，外发鑫达工厂，红色M码200件L码150件，5月1日开始，5月30日交期」

---

**方式三：裁剪订单创建（裁剪模块专用）**

入口：裁剪管理 → 创建裁剪订单

操作步骤：
1. 进入裁剪管理模块
2. 点击"创建裁剪订单"
3. 输入款号（支持手输新号，系统自动创建最小化款式记录）
4. 选择工厂类型（内部/外发）和具体工厂
5. 上传款式封面图
6. 填写颜色×尺码×数量明细
7. 设置下单日期和交货日期
8. 工序工作流自动从模板库获取，无模板时使用默认6大节点（采购→裁剪→二次工艺→车缝→尾部→入库）
9. 提交后自动：创建生产订单 + 创建裁剪任务 + 初始化扫码记录 + 重算进度
10. 订单号格式：CUT + 时间戳（可自定义）

特点：
- materialArrivalRate 默认 100%（裁剪订单默认物料已到位）
- 款号不存在时自动创建最小化款式（含封面图）

问小云：「在裁剪模块帮我创建个订单，款号0099988，外发鑫达工厂」

---

**方式四：样衣推送到下单管理**

入口：款式资料页 → 选品管理 → 推送到下单管理

操作步骤：
1. 款式完成样衣开发后，进入款式详情
2. 点击"推送到下单管理"
3. 勾选要同步的数据：BOM用料、尺码表、工序列表、工序单价、进度模板
4. 系统自动：标记款式为"可下单"状态，同步数据到模板库和资料中心

适用场景：样衣开发完成 → 进入大货生产阶段

注意：推送后仍需在下单管理页面手动创建订单

---

**方式五：OpenAPI外部对接（客户系统自动同步）**

入口：外部系统 → POST /openapi/v1/order/create

特点：
- 客户ERP/电商平台自动推送订单，无需手动录入
- 支持单条创建和批量上传
- HMAC-SHA256 签名鉴权（X-App-Key + X-Timestamp + X-Signature）
- 返回订单号和状态

适用场景：与客户系统（ERP/电商平台）打通，自动接收订单

---

**方式六：复制订单（同款不同色/不同码快速创建）**

入口：订单列表 → 操作 → 复制

操作步骤：
1. 在订单管理页找到已有订单
2. 点击"复制"
3. 系统自动复制：款号、款名、工厂、SKU明细、工序工作流
4. 状态重置为pending，进度归零
5. 用户修改颜色/尺码/数量后提交

适用场景：同款不同色/不同码快速下单，免去重复填写

---

